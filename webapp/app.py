@app.route("/api/genes")
def api_genes():
    """Return gene index with metadata."""
    idx_path = PROFILES_DIR / "_gene_index.json"
    if not idx_path.exists():
        return jsonify({"error": "Gene index not found. Run 07_ad_speed_profiles.py first."}), 404
    genes = _safe_load(idx_path)
    return jsonify(genes)


@app.route("/api/gene/<path:gene_file>")
def api_gene(gene_file):
    """Return full profile data for a single gene."""
    path = PROFILES_DIR / gene_file
    if not path.exists():
        return jsonify({"error": f"Profile not found: {gene_file}"}), 404
    data = _safe_load(path)
    return jsonify(data)


@app.route("/api/correlations")
def api_correlations():
    """Return cross-method correlation summary."""
    path = PROFILES_DIR / "_correlations.json"
    if not path.exists():
        return jsonify({"error": "Correlations not found."}), 404
    data = _safe_load(path)
    return jsonify(data)


@app.route("/api/figure/<fname>")
def api_figure(fname):
    """Serve result figures."""
    return send_from_directory(str(RESULTS_DIR), fname)


@app.route("/presentation/<path:fname>")
def presentation(fname):
    """
    Serve PDF presentations and speaker notes from presentation/.
    Forces Content-Disposition: attachment so the browser downloads the file.
    Whitelists *.pdf only.
    """
    if not fname.lower().endswith(".pdf"):
        return jsonify({"error": "Only .pdf downloads are allowed here."}), 400
    return send_from_directory(
        str(PRESENTATION_DIR),
        fname,
        as_attachment=True,
        mimetype="application/pdf",
    )


@app.route("/api/presentation")
def api_presentation():
    """
    List PDFs available in presentation/ with size and mtime so the UI can
    offer fresh download links.
    """
    if not PRESENTATION_DIR.exists():
        return jsonify({"files": []})
    files = []
    for p in sorted(PRESENTATION_DIR.glob("*.pdf")):
        stat = p.stat()
        files.append({
            "name":       p.name,
            "url":        f"/presentation/{p.name}",
            "size_bytes": stat.st_size,
            "size_mb":    round(stat.st_size / (1024 * 1024), 2),
            "mtime":      datetime.utcfromtimestamp(stat.st_mtime).isoformat() + "Z",
        })
    return jsonify({"files": files})


