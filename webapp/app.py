"""
AD Speed Profile Viewer — Flask Web App (v2)
=============================================
Serves interactive per-gene RNAPII instantaneous speed profiles generated by
07_ad_speed_profiles.py, plus research dashboard with strategy log, todos,
epigenomics, temporal rephasing, and pipeline status.

Three speed estimation approaches are visualized side by side:
  A: v(p) = C / rho_40m(p)         — raw 40m coverage
  B: v(p) = C / (rho_40m - rho_0m) — differential coverage
  C: multi-timepoint product group  — Z_M × Z_3 average

New endpoints in v2:
  GET /api/strategy   — parsed STRATEGY_LOG.md sessions + current results
  GET /api/todos      — active/completed TODO items from STRATEGY_LOG.md
  GET /api/status     — pipeline status (which data exists)
  GET /api/epigenomics/<gene_file> — per-gene epigenomic marks (if available)
  GET /api/temporal/<gene_file>    — temporal rephased profiles (if available)

Run:
  cd <project>/webapp
  python3 app.py

Then open: http://localhost:5050
"""

