@app.route("/api/strategy")
def api_strategy():
    """
    Parse STRATEGY_LOG.md and return structured JSON with sessions and results.
    """
    parsed = _parse_strategy_log()
    if parsed is None:
        return jsonify({"error": "STRATEGY_LOG.md not found."}), 404

    return jsonify({
        "sessions":        parsed["sessions"],
        "current_results": parsed["current_results"],
    })


@app.route("/api/todos")
def api_todos():
    """
    Parse STRATEGY_LOG.md for Markdown task list items and return them.
    Active items: - [ ] text
    Completed items: - [x] text
    """
    parsed = _parse_strategy_log()
    if parsed is None:
        return jsonify({"error": "STRATEGY_LOG.md not found."}), 404

    return jsonify(parsed["todos"])


@app.route("/api/status")
def api_status():
    """
    Return pipeline status: which data types have been generated.
    """
    return jsonify(_pipeline_status())


@app.route("/api/epigenomics/<path:gene_file>")
def api_epigenomics(gene_file):
    """
    Return per-gene epigenomic mark data from results/epigenomics/.
    Returns 404 with a helpful message if epigenomics have not been downloaded.
    """
    if not EPIGENOMICS_DIR.exists():
        return jsonify({
            "error": "Epigenomics directory not found.",
            "hint":  "Run scripts/10_download_epigenomics.py to fetch ENCODE marks.",
            "marks": ["methylation", "H3K36me3", "H3K27ac", "H3K4me3", "H3K27me3", "CTCF"],
        }), 404

    path = EPIGENOMICS_DIR / gene_file
    if not path.exists():
        return jsonify({
            "error": f"Epigenomics profile not found: {gene_file}",
            "hint":  "Run scripts/10_download_epigenomics.py to generate profiles.",
        }), 404

    data = _safe_load(path)
    return jsonify(data)


@app.route("/api/temporal/<path:gene_file>")
def api_temporal(gene_file):
    """
    Return temporal rephased profile for a gene from results/temporal_profiles/.
    Returns 404 with a helpful message if temporal rephasing has not been run.
    """
    if not TEMPORAL_DIR.exists():
        return jsonify({
            "error": "Temporal profiles directory not found.",
            "hint":  "Run scripts/11_temporal_rephase.py to convert genomic → time coordinates.",
        }), 404

    path = TEMPORAL_DIR / gene_file
    if not path.exists():
        return jsonify({
            "error": f"Temporal profile not found: {gene_file}",
            "hint":  "Run scripts/11_temporal_rephase.py to generate this profile.",
        }), 404

    data = _safe_load(path)
    return jsonify(data)


